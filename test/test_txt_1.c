    dns->raw = packet_bytes;
    dns->raw_len = sizeof(packet_bytes);
    assert(sdns_from_wire(dns) == 0);
    sdns_rr_TXT * txt = sdns_decode_rr_TXT(dns, dns->msg->answer);
    assert(txt != NULL);
    assert(strncmp(txt->character_string.content, "google-site-verification=wGLZMNWYv3IiNCZ9zxM0hfZlzqGloyAQ_y2r7OV_vJ8", txt->character_string.len) == 0);
