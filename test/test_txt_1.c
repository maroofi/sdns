    dns->raw = packet_bytes;
    dns->raw_len = sizeof(packet_bytes);
    assert(sdns_from_wire(dns) == 0);
    sdns_rr_TXT * txt = sdns_decode_rr_TXT(dns, dns->msg->answer);
    assert(txt != NULL);
    char first_txt[] = {0xD8, 0xB3, 0xD9, 0x88, 0xD8, 0xB1, 0xD9, 0x86, 0xD8, 0xA7};
    assert(strncmp(txt->character_string.content, first_txt, sizeof(first_txt)) == 0);
